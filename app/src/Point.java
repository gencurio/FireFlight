import static java.lang.Math.sqrt;
import static java.lang.Math.atan;
class Point
{
    private final double x;
    private final double y;
    private final double z;



    public Point(double x, double y)
    {
        this.x = x;
        this.y = y;
        this.z = z;

    }

    public double getX()
    {
        return x;
    }

    public double getY()
    {
        return y;
    }
    
    public double getZ() { return z; }



}